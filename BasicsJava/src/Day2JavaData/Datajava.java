//Program to demonstrate char data type
package Day2JavaData;

public class Datajava {

	public static void main(String[] args) 
	{
		// assigning single character literal
		char ch = 'a';
		System.out.println(ch) ;

		//assigning number to char
		char ch1= 65 ;
		System.out.println(ch1) ;
		
		//assigning unique code to char
		char var1 = '\u00A7';
		System.out.println (var1) ;
		
		//Unique code representation
				char var2 = '\u20AC' ;
				System.out.println (var2 );
				
		//ASCII code representation
				int a = 'A';
				System.out.println(a) ;		
				
	}

}
